class Calculator:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    #addition
    def add(self):
        return self.a+self.b
    #subtraction
    def sub(self):
        return self.a-self.b
    #multiplication
    def mul(self):
        return self.a*self.b
    #division
    def div(self):
        return self.a/self.b
    
